package com.abdulbari.strings;

public class printing {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);
        System.out.printf("%d", a);
        System.out.println();
        System.out.printf("%5d", a);
        System.out.println();
        System.out.printf("%05d", a);
    }
}
